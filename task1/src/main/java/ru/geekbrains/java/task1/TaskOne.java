package ru.geekbrains.java.task1;

public class TaskOne {

    public static void main(String[] args) {
        System.out.println("Задание 1");
            boolean a = false; // Переменные этого типа могут принимать всего два значения – true (истина/правда) и false (ложь) //
            byte b = 0; // Это знаковый 8-битовый тип //
            char c = 'x'; // 16-разрядное беззнаковое целое, представляющее собой символ UTF-16 (буквы и цифры) //
            short d = 100; // 16-разрядное целое число //
            int i = 277; // 32-разрядное целое число //
            long f = 475395; // 64-разрядное целое число //
            float g = 891.0f; // 32-разрядное число в формате IEEE 754 с плавающей точкой //
            double h = 631.000; // 64-разрядное число в формате IEEE 754 с плавающей точкой //
            System.out.println("Значение boolean = " + a);
            System.out.println("Значение byte = " + b);
            System.out.println("Значение char = [" + c + "]");
            System.out.println("Значение short = " + d);
            System.out.println("Значение int = " + i);
            System.out.println("Значение long = " + f);
            System.out.println("Значение float = " + g);
            System.out.println("Значение double = " + h);

    }
}



